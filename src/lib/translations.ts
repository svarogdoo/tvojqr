export function t(
  lang: "en" | "sr" | "ru" | "el",
  key: string,
  defaultValue: string = ""
): string {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    value = value?.[k];
  }

  return value || defaultValue;
}
